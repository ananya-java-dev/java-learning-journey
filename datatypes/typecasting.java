package datatypes;
// type casting basic
public class typecasting {
     public static void main(String[] args) {
         //implicit conversion
         int a =98;
         double b = a;
         System.out.println(b);
         // explicit conversion
         float x = 76.988765f;
         int y=(int)(x);
         System.out.println(y);


    }
}
